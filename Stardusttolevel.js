function StardustToLevel(Stardust){
  if(Stardust==200) return [1,2.5]
  else if(Stardust==400) return [3,4.5]
  else if(Stardust==600) return [5,6.5]
  else if(Stardust==800) return [7,8.5]
  else if(Stardust==1000) return [9,10.5]
  else if(Stardust==1300) return [11,12.5]
  else if(Stardust==1600) return [13,14.5]
  else if(Stardust==1900) return [15,16.5]
  else if(Stardust==2200) return [17,18.5]
  else if(Stardust==2500) return [19,20.5]
  else if(Stardust==3000) return [21,22.5]
  else if(Stardust==3500) return [23,24.5]
  else if(Stardust==4000) return [25,26.5]
  else if(Stardust==4500) return [27,28.5]
  else if(Stardust==5000) return [29,30.5]
  else if(Stardust==6000) return [31,32.5]
  else if(Stardust==7000) return [33,34.5]
  else if(Stardust==8000) return [35,36.5]
  else if(Stardust==9000) return [37,38.5]
  else if(Stardust==10000) return [39,40]
}
